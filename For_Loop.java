public class For_Loop {
    public static void main(String[] args) {
        // for loop ----> executes a block of code only limited amount of times as we specify
        // while, while loop goes for unlimited times
        // lets take a loop to syntax
        for (int i=3; i>=0; i--){
            System.out.print(i +" \t" );
        }
        System.out.println("\n Happy learning, \n \t Enjoyable time");
    }
}
