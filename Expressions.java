public class Expressions {
    public static void main(String[] args) {
        /*
        * expressions = operators and operands
        * operators = values, variables, numbers, quantity
        * operators = + - * / %
        */
        int friends = 10; // suppose I have 10 friends
        friends = friends + 3 ; // now I have make more three friends, so they becomes my friend plus 3
        System.out.println("Total number of my friends is: \t" +friends );
    //---------------------------------------------------------------------------------------------------------------//
        // again i need to divide my friends into three groups lets see how can i do it..^_^
        friends = friends / 3 ;
        System.out.println("number of my friends in each group is: \t" +friends);
        // similarly, we can do more.... by changing the operators that we need
        // % would return reminder
        // - will difference
        // / will divide
        // * will multiply
        // + will add
    }
}
